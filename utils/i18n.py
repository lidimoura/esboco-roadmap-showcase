    selected_lang = st.sidebar.selectbox("Idioma", options=list(lang_options.keys()), format_func=lambda x: lang_options[x])
    
    if selected_lang != st.session_state.lang:
        st.session_state.lang = selected_lang
        st.experimental_rerun()

